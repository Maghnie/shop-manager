def add_section_divider(prs, section_number, section_title, description=""):
    """Create section divider slide"""
    slide_layout = prs.slide_layouts[5]  # Blank layout
    slide = prs.slides.add_slide(slide_layout)

    # Add colored background shape
    shapes = slide.shapes
    background = shapes.add_shape(
        1,  # Rectangle
        Inches(0), Inches(0),
        prs.slide_width, prs.slide_height
    )
    background.fill.solid()
    colors = [
        RGBColor(0, 51, 102),    # Dark blue
        RGBColor(41, 128, 185),  # Blue
        RGBColor(39, 174, 96),   # Green
        RGBColor(142, 68, 173),  # Purple
        RGBColor(230, 126, 34),  # Orange
        RGBColor(192, 57, 43),   # Red
    ]
    background.fill.fore_color.rgb = colors[(section_number - 1) % len(colors)]

    # Add section number
    number_box = shapes.add_textbox(
        Inches(0.5), Inches(2.5),
        Inches(2), Inches(1.5)
    )
    tf = number_box.text_frame
    p = tf.paragraphs[0]
    p.text = str(section_number)
    p.font.size = Pt(120)
    p.font.bold = True
    p.font.color.rgb = RGBColor(255, 255, 255)
    p.alignment = PP_ALIGN.CENTER

    # Add section title
    title_box = shapes.add_textbox(
        Inches(3), Inches(2.5),
        Inches(6.5), Inches(1)
    )
    tf = title_box.text_frame
    p = tf.paragraphs[0]
    p.text = section_title
    p.font.size = Pt(48)
    p.font.bold = True
    p.font.color.rgb = RGBColor(255, 255, 255)
    p.alignment = PP_ALIGN.RIGHT

    # Add description if provided
    if description:
        desc_box = shapes.add_textbox(
            Inches(3), Inches(3.8),
            Inches(6.5), Inches(1)
        )
        tf = desc_box.text_frame
        p = tf.paragraphs[0]
        p.text = description
        p.font.size = Pt(24)
        p.font.color.rgb = RGBColor(255, 255, 255)
        p.alignment = PP_ALIGN.RIGHT

    add_home_button_note(slide)
    return slide


def add_content_slide(prs, title_text, content_items, image_description=None):
    """Create content slide with bullet points and optional image placeholder"""
    slide_layout = prs.slide_layouts[1]
    slide = prs.slides.add_slide(slide_layout)

    title = slide.shapes.title
    title.text = title_text
    title.text_frame.paragraphs[0].alignment = PP_ALIGN.RIGHT
    title.text_frame.paragraphs[0].font.color.rgb = RGBColor(0, 51, 102)

    # If image is requested, adjust layout
    if image_description:
        # Content on left, image placeholder on right
        content_left = Inches(0.5)
        content_top = Inches(1.5)
        content_width = Inches(5)
        content_height = Inches(5)

        # Add text box for content
        content_box = slide.shapes.add_textbox(
            content_left, content_top,
            content_width, content_height
        )
        tf = content_box.text_frame
        tf.word_wrap = True

        # Add image placeholder
        img_left = Inches(6)
        img_top = Inches(1.5)
        img_width = Inches(3.5)
        img_height = Inches(5)

        img_box = slide.shapes.add_shape(
            1,  # Rectangle
            img_left, img_top,
            img_width, img_height
        )
        img_box.fill.solid()
        img_box.fill.fore_color.rgb = RGBColor(240, 240, 240)
        img_box.line.color.rgb = RGBColor(200, 200, 200)

        # Add text to image placeholder
        img_tf = img_box.text_frame
        img_tf.word_wrap = True
        img_p = img_tf.paragraphs[0]
        img_p.text = f"[ุตูุฑุฉ ุชูุถูุญูุฉ]\n\n{image_description}"
        img_p.font.size = Pt(14)
        img_p.font.italic = True
        img_p.alignment = PP_ALIGN.CENTER
        img_p.font.color.rgb = RGBColor(100, 100, 100)
    else:
        # Use default content placeholder
        content = slide.placeholders[1]
        tf = content.text_frame
        tf.clear()

    # Add content items
    for item in content_items:
        p = tf.add_paragraph()
        if isinstance(item, dict):
            p.text = item['text']
            p.level = item.get('level', 0)
            p.font.size = Pt(item.get('size', 18))
            p.font.bold = item.get('bold', False)
        else:
            p.text = item
            p.font.size = Pt(18)
            p.level = 1 if item.startswith("โข") else 0
        p.alignment = PP_ALIGN.RIGHT

    add_home_button_note(slide)
    return slide


